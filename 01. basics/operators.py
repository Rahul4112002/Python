"""Operators in Python"""

a = 5
b = 32

# Arithmetic operators
print(a + b)
print(b - a)
print(a * b)
print(b // a)
print(b / a)
print(5 ** 3)
print(32 % 5)

# Comparison operators
a = 12.1
b = 12
print(a == b)
print(a != b)
print(a > b)
print(45 < 67)
print(23 >= 23)
print(45 <= 45)

# Logical operators
print(12 > 20 and 123 > 100 and 34 == 34 and 45 < 90)
print(12 != 12 or 23 == 45 or 67 == 56 or 10 > 5)
print(not 12 == 12)
